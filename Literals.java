public class Literals {
    public static void main(String args[]) {

        //binary
        int num = 0b110;
        System.out.println(num);

        //hexadecimal
        int num1 = 0x7E;
        System.out.println(num1);

        //Counting number of zeros
        int num2 = 1_00_00_00_000;
        System.out.println(num2);

        //double
        double num3 = 59;
        System.out.println(num3);
    }
    
}
