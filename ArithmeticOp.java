public class ArithmeticOp {
    public static void main(String[] args) {
        int a = 7;
        int b =5;
        int add = a+b;
        System.out.println(add);

        int sub = a-b;
        System.out.println(sub);

        int prod = a*b;
        System.out.println(prod);

        int div = a/b;
        System.out.println(div);

        int mod = a % b;
        System.out.println(mod);

    }
}